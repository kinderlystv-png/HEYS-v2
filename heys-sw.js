// Cache First - —Å–Ω–∞—á–∞–ª–∞ –∫–µ—à, –ø–æ—Ç–æ–º —Å–µ—Ç—å
async function cacheFirst(request) {
  try {
    const cacheResponse = await caches.match(request);
    if (cacheResponse) {
      console.log('[SW] –û—Ç–≤–µ—Ç –∏–∑ –∫–µ—à–∞:', request.url);
      return cacheResponse;
    }

    console.log('[SW] –ó–∞–ø—Ä–æ—Å –≤ —Å–µ—Ç—å:', request.url);
    const networkResponse = await fetch(request);

    if (networkResponse.ok) {
      const cache = await caches.open(STATIC_CACHE);
      cache.put(request, networkResponse.clone());
    }

    return networkResponse;
  } catch (error) {
    console.error('[SW] Cache First –æ—à–∏–±–∫–∞:', error);
    return new Response('–û—Ñ–ª–∞–π–Ω', { status: 503 });
  }
}

