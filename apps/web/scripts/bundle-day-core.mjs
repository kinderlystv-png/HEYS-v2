// bundle-day-core.mjs — собирает 7 source-файлов Day-core в heys_day_core_bundle_v1.js.
//
// Бандл входит в boot-calc (scripts/legacy-bundle-config.mjs), выполняется в runtime.
// Раньше был manual concat — drift между source-файлами и embedded-секциями привёл
// к 6 из 7 расхождений (e.g., -1387 строк в utils embedded vs source). Теперь
// source = single source of truth, бандл генерируется.
//
// Список источников = scripts/legacy-bundle-config.mjs → LEGACY_GENERATORS['day-core'].sources.
//
// Запуск:
//   node apps/web/scripts/bundle-day-core.mjs
//   через predev/prebuild и pre-commit auto-sync
//
// Output: apps/web/heys_day_core_bundle_v1.js (auto-generated, не редактируй вручную).

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

import { LEGACY_GENERATORS } from '../../../scripts/legacy-bundle-config.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const WEB_ROOT = path.resolve(__dirname, '..');
const OUT_FILE = path.join(WEB_ROOT, 'heys_day_core_bundle_v1.js');

const sources = LEGACY_GENERATORS['day-core'].sources;

const banner = [
    '// heys_day_core_bundle_v1.js — Day core bundle (utils/hooks/calculations/effects/handlers)',
    '// ⚠️ Auto-generated by apps/web/scripts/bundle-day-core.mjs. Do not edit manually.',
    "// Sources: scripts/legacy-bundle-config.mjs → LEGACY_GENERATORS['day-core'].sources",
].join('\n');

const sectionBlocks = sources.map((sourcePath) => {
    const relativeToWeb = sourcePath.startsWith('apps/web/')
        ? sourcePath.slice('apps/web/'.length)
        : sourcePath;
    const absPath = path.join(WEB_ROOT, relativeToWeb);

    if (!fs.existsSync(absPath)) {
        throw new Error(`[bundle-day-core] Missing source file: ${sourcePath}`);
    }

    const raw = fs.readFileSync(absPath, 'utf8').replace(/\n+$/, '');
    const fileName = path.basename(sourcePath);
    return `// === ${fileName} ===\n${raw}`;
});

const output = banner + '\n\n' + sectionBlocks.join('\n\n') + '\n';
fs.writeFileSync(OUT_FILE, output, 'utf8');

console.info(`[bundle-day-core] ✅ Generated ${OUT_FILE} (${sources.length} modules)`);
