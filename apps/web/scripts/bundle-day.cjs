const fs = require('fs');
const path = require('path');

const ROOT = process.cwd();
const OUT_FILE = path.join(ROOT, 'heys_day_bundle_v1.js');

const modules = [
    { file: 'day/_advice.js', label: 'day/_advice.js' },
    // day/_meal_quality.js остался eager даже после P0-D-stretch:
    // heys_day_tab_impl_v1.js делает module-load destructuring HEYS.mealScoring
    // (lines 31-55), и 3 функции реально используются runtime в render
    // (getMealQualityScore, getDailyNutrientColor, getDailyNutrientTooltip).
    // Если бы перенесли в lazy — closure захватил бы undefined.
    { file: 'day/_meal_quality.js', label: 'day/_meal_quality.js' },
    // heys_day_meals_bundle_v1.js переехал из boot-calc inline в
    // postboot-3-ui-lazy (см. scripts/legacy-bundle-config.mjs).
    // Освободили ~430KB критического пути; meals UI подгружается lazy
    // через postboot-3 facade. Skeleton "Загрузка дневника…" в DayTab держит UX
    // пока chunk не приехал (heys_day_tab_impl_v1.js:575).
];

const banner = [
    '// heys_day_bundle_v1.js — bundled day modules (advice only after P0-D-stretch)',
    '// ⚠️ Auto-generated by scripts/bundle-day.cjs. Do not edit manually.',
    ''
].join('\n');

const chunks = [banner];

modules.forEach((mod) => {
    const srcPath = path.join(ROOT, mod.file);
    if (!fs.existsSync(srcPath)) {
        throw new Error(`[bundle-day] Missing source file: ${mod.file}`);
    }

    const raw = fs.readFileSync(srcPath, 'utf8');
    const content = raw.trimEnd();
    const safeContent = content.startsWith(';') ? content : `;${content}`;

    chunks.push(`// ===== Begin ${mod.label} =====`);
    chunks.push(safeContent);
    chunks.push(`// ===== End ${mod.label} =====`);
    chunks.push('');
});

fs.writeFileSync(OUT_FILE, chunks.join('\n'), 'utf8');
