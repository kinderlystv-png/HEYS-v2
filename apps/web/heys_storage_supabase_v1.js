      log('üîÑ [CLIENT_SYNC] Loading data for client:', client_id);
      const { data, error } = await client.from('client_kv_store').select('k,v,updated_at').eq('client_id', client_id);
      if (error) { err('client bootstrap select', error); return; }
      
