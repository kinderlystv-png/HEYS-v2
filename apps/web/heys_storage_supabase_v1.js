          // For client-scoped keys like 'heys_products', we need to store them with client_id prefix
          let key = row.k;
          
          // –ï—Å–ª–∏ –∫–ª—é—á 'heys_products' (–±–µ–∑ client_id), –¥–æ–±–∞–≤–ª—è–µ–º client_id
