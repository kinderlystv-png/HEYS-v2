// landing-variants.ts — A/B/C/D тестирование лендинга
// Четыре тональности:
// A: premium (минимализм, делегирование)
// B: direct (жёстко по сути, механика)
// C: honest (анти-обещания, протокол)
// D: original (классический эмоциональный)

export type LandingVariant = 'A' | 'B' | 'C' | 'D'

export type LandingSectionId =
  | 'socialProof'
  | 'delegate'
  | 'interaction'
  | 'formats'
  | 'problem'
  | 'solution'
  | 'howItWorks'
  | 'layers'
  | 'screens'
  | 'matrix'
  | 'cases'
  | 'doDont'
  | 'method'
  | 'sla'
  | 'security'
  | 'pricing'
  | 'trial'
  | 'purchase'
  | 'faq'
  | 'footer'

export interface NavLink {
  id: string
  label: string
  href: string
}

export interface VariantContent {
  id: LandingVariant
  name: string
  description: string
  hero: {
    headline: string
    subheadline: string
    features?: string[] // H3 — пункты под subheadline
    ctaPrimary: string
    ctaSecondary: string
    frictionNote?: string // Снижение трения рядом с CTA
    microtext: string
    microtextLine2?: string // Вторая строка microtext
  }
  nav: {
    links: NavLink[]
  }
  page: {
    order: LandingSectionId[]
  }
  socialProof: {
    eyebrow?: string
    title: string
    quotes: string[]
  }
  delegate: {
    eyebrow?: string
    title: string
    cards: { title: string; desc: string; icon: string }[]
  }
  interaction: {
    eyebrow?: string
    title: string
    steps: { title: string; desc: string; icon: string }[]
    note?: string
  }
  formats: {
    eyebrow?: string
    title: string
    cards: { title: string; desc: string; points: string[]; highlight?: boolean }[]
  }
  layers: {
    eyebrow?: string
    title: string
    items: { title: string; desc: string; icon: string }[]
  }
  screens: {
    eyebrow?: string
    title: string
    subtitle?: string
    items: { title: string; benefit: string }[]
  }
  matrix: {
    eyebrow?: string
    title: string
    subtitle?: string
    rows: {
      label: string
      base: string
      pro: string
      proPlus: string
    }[]
  }
  cases: {
    eyebrow?: string
    title: string
    items: { before: string; after: string }[]
  }
  doDont: {
    eyebrow?: string
    title: string
    do: string[]
    dont: string[]
  }
  method: {
    eyebrow?: string
    title: string
    steps: { title: string; desc: string }[]
  }
  sla: {
    eyebrow?: string
    title: string
    items: { label: string; value: string }[]
    protocol: { icon: string; label: string; desc: string }[]
    note?: string
  }
  security: {
    eyebrow?: string
    title: string
    bullets: string[]
    links: { label: string; href: string }[]
  }
  faq: {
    eyebrow?: string
    title: string
    subtitle?: string
    items: { q: string; a: string }[]
  }
  trial: {
    badge: string
    title: string
    subtitle: string
    bullets: { icon: string; text: string }[]
    limitation: string
    startPoint: string
    ctaAvailable: string
    ctaQueue: string
    purchaseLinkText: string
    purchaseLinkCta: string
  }
  pricing: {
    intro: string
    formatSelf: { title: string; desc: string }
    formatConcierge: { title: string; desc: string }
  }
}

